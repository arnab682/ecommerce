<?php

namespace App\Http\Controllers;

use App\Model\Subscriber;
use Illuminate\Http\Request;

class SubscriberController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
      try{

          $subscribers = Subscriber::all();
          return view('table.subscribers.index', compact('subscribers'));

      }catch(QueryException $e){

          return redirect()
              ->route('404_blade')
              ->withInput()
              ->withErrors($e->getMessage());
      }
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
          return view('table.subscribers.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      try {

          $data = $request->all();

          Subscriber::create($data);

          //return redirect()->route('labs.index')->withMessage('Lab is Inserted Successfully.');
          //return redirect()->route('slider.index')->with('message','Slider is Inserted Successfully.');
          return back();

      }catch(QueryException $e){

          return redirect()
              ->route('subscriber.create')
              ->withInput()
              ->withErrors($e->getMessage());
      }
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
      $subscriber = Subscriber::findOrfail($id);
      //dd($slider);
      return view('table.subscribers.show', compact('subscriber'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
      $subscriber = Subscriber::findOrfail($id);
      return view('table.subscribers.edit', compact('subscriber'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
      $subscriber = Subscriber::findOrFail($id);
      $data = $request->all();
      $subscriber->update($data);

      return redirect('subscriber')->with('message','Subscriber Updated !');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
      try{
          $subscriber = Subscriber::findOrFail($id);
          $subscriber->delete();
          return redirect()->route('subscriber.index')->with('message','Subscriber is Deleted Successfully.');
        }catch(QueryException $e){
          return redirect()
              ->route('subscriber.index')
              ->withErrors($e->getMessage());
        }
    }
}
